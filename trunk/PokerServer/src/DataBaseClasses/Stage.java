package DataBaseClasses;

/**
 *
 * @author Андрей
 */
public class Stage {
    private int id;
    private String name;
    private int time;
    public Stage(int id, String name, int time){
        this.id=id;
        this.name=name;
        this.time=time;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getTime() {
        return time;
    }

    public void setTime(int time) {
        this.time = time;
    }
}
